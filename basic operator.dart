void main() {
  int x = 50;
  int y = 10;
  int sum = x + y;
  int difference = x - y;
  int product = x * y;
  double quotient = x / y;

  print("The sum of $x and $y is $sum");
  print("The difference when $y is subtracted from $x is $difference");
  print("The product of $x and $y is $product");
  print("The quotient when $x is divided by $y is $quotient");
}
